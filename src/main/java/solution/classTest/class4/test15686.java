package solution.classTest.class4;

import solution.levelTest.level11.번외_mergeSort;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.LinkedList;
import java.util.StringTokenizer;

public class test15686 {

    static int N;
    static int M;
    static int[][] town;
    static ArrayList<Integer[]> shops = new ArrayList<>();
    static boolean[] visited;
    static int shopTotal = 0;
    static ArrayList<Integer[]> homes = new ArrayList<>();
    static int answer = Integer.MAX_VALUE;

    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        StringTokenizer st = new StringTokenizer(br.readLine());

        N = Integer.parseInt(st.nextToken());
        M = Integer.parseInt(st.nextToken());

        town = new int[N][N];

        for(int i = 0 ; i<N; i++){
            st = new StringTokenizer(br.readLine());
            for(int j = 0 ; j<N; j++){
                int data = Integer.parseInt(st.nextToken());
                if(data == 2){
                    shops.add( new Integer[]{i,j} );
                    town[i][j] = 0;
                    shopTotal++;
                }else if(data == 1){
                    town[i][j] = data;
                    homes.add(new Integer[]{i,j});
                }else{
                    town[i][j] = data;
                }
            }
        }

        visited = new boolean[shopTotal];
        dfs(0, 0);

        System.out.println(answer);
    }

    public static void dfs(int depth, int idx ){
        if(depth == M){
            int[][] chart = new int[N][N];
            for(int i = 0 ; i<N; i++){
                Arrays.fill(chart[i], Integer.MAX_VALUE);
            }
            LinkedList<Integer[]> queue = new LinkedList<>();
            boolean[][] visitQ = new boolean[N][N];

            for(int i = 0; i<N; i++ ){
                for(int j = 0 ; j<N; j++){
                    if(town[i][j] == 2){
                        visitQ[i][j] = true;
                        queue.add(new Integer[]{i,j,0});
                        chart[i][j] = 0;
                        setCD(chart,queue, visitQ);
                        visitQ = new boolean[N][N];
                    }
                }
            }
            int sum = 0;
            for(int i = 0 ; i<N; i++){
                for( int j = 0 ; j<N; j++){
                    if(town[i][j]==1){
                        sum += chart[i][j];
                    }
                }
            }
            answer = Math.min(sum, answer);

            return;
        }

        for(int i = idx ; i<shopTotal; i++){
            if(!visited[i]){
                visited[i] = true;
                Integer[] position = shops.get(i);
                town[position[0]][position[1]] = 2;
                dfs(depth+1, i);
                visited[i] = false;
                town[position[0]][position[1]] = 0;
            }
        }
    }

    public static void setCD(int[][] chart, LinkedList<Integer[]> queue, boolean[][] visitQ){
        if(queue.isEmpty()){
            return;
        }
        Integer[] position = queue.pop();
        int row = position[0];
        int col = position[1];
        int d = position[2]+1;

        if(row-1>=0 && !visitQ[row-1][col] && chart[row-1][col] > d ){
            visitQ[row-1][col] = true;
            chart[row-1][col] = d;
            queue.add( new Integer[]{row-1, col, d} );
        }
        if(row+1<N && !visitQ[row+1][col] && chart[row+1][col] > d ){
            visitQ[row+1][col] = true;
            chart[row+1][col] = d;
            queue.add( new Integer[]{row+1, col, d} );
        }
        if(col-1>=0 && !visitQ[row][col-1] && chart[row][col-1] > d ){
            visitQ[row][col-1] = true;
            chart[row][col-1] = d;
            queue.add( new Integer[]{row, col-1, d} );
        }
        if(col+1<N && !visitQ[row][col+1] && chart[row][col+1] > d ){
            visitQ[row][col+1] = true;
            chart[row][col+1] = d;
            queue.add( new Integer[]{row, col+1, d} );
        }
        setCD(chart, queue, visitQ);
    }
}
